using System;
using System.Collections.Generic;

public class ArrayHelper
{
    public List<int> getRange(List<int> source, int start, int length)
    {
        return source.GetRange(start, length);
    }

    public List<string> getRange(List<string> source, int start, int length)
    {
        return source.GetRange(start, length);
    }
    
    public List<float> getRange(List<float> source, int start, int length)
    {
        return source.GetRange(start, length);
    }
    
    public List<double> getRange(List<double> source, int start, int length)
    {
        return source.GetRange(start, length);
    }

    public List<long> getRange(List<long> source, int start, int length)
    {
        return source.GetRange(start, length);
    }

    public List<object> getRange(List<object> source, int start, int length)
    {
        return source.GetRange(start, length);
    }
}